                <h3>Diabetes report 2000 — 2045</h3>
                <p>
                    <span>Estimates</span> were <span>made for 38 countries and territories </span>in the IDF Western Pacific (WP) Region. For this edition of the IDF Diabetes Atlas, <span>41 data sources from 28 countries</span> were <span>used to generate estimates of diabetes in adults aged 20-79</span> years. Estimates for China, Indonesia, Malaysia and Marshall Islands were based on studies conducted within the past five years.
                </p>
                <p>
                    <span>The WP Region accounts </span> for <span>over a third (38%) of the total number of adults living with diabetes. </span><span>WP Region</span> has <span>the third highest prevalence of diabetes (11.9%) in the world</span>. IDF projects that the number of <span> people with diabetes in WP Region will increase 27% reaching 260 million by 2045 and the prevalence of diabetes will increase 21% reaching 14.4% in 2045</span>. The proportion of undiagnosed diabetes is second highest in the WP Region with 52.9%. Diabetes is responsible for 2.3 million deaths in 2021, which is the highest number of all IDF Regions. Diabetes-related expenditure in 2021 totals USD241 billion, representing 25% of global expenditure.
