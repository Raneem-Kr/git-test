                    <h2>Symptoms and Causes</h2>
                    <h3>What are the symptoms of prediabetes?</h3>
                    <p class="paragraph">
                        Most people with prediabetes <span> don’t</span> have <span>any symptoms</span>. This is why
                        it’s important to <span>see</span> your primary <span>care provider</span> regularly so they
                        can do screenings, like a basic metabolic panel, to <span>check</span> on your
                        <span>blood sugar levels</span>. This is the <span>only way </span>to know if you have
                        prediabetes.
                    </p>
                    <h6>
                        For the few people who do experience symptoms of prediabetes, they
                        may include:
                    </h6>
                    <ul class="p-3">
                        <li>
                            Darkened skin in your armpit or back and sides of your neck
                            (acanthosis nigricans).
                        </li>
                        <li>Eye changes that can lead to diabetes-related retinopathy.</li>
                        <li>Skin tags</li>
                    </ul>
                </div>
                <div class="container-fluid">
                    <div class="row">
                        <div class="col-md-8">
                            <h3>What causes prediabetes?</h3>
                            <p>
                                The cause of prediabetes is <span>the same</span> as the cause of <span>Type 2
                                    diabetes</span> — mainly, <span>insulin resistance</span>. Insulin resistance
                                happens when cells in your muscles, fat and liver <span>don’t respond</span>
                                as they should to insulin. <span>Insulin </span>is a hormone your pancreas
                                makes that’s <span>essential </span>for life and regulating blood sugar
                                levels. When you don’t have <span>enough insulin</span> or your body <span>doesn’t
                                    respond properly</span> to it, you experience elevated blood sugar
                                levels. Several factors can contribute to insulin resistance,
                                including:
                            </p>
                            <ul class="pl-3">
                                <li>Genetics.</li>
                                <li>Excess body fat, especially in your belly and around your organs (visceral fat).</li>
                                <li>Physical inactivity.</li>
                                <li>Eating highly processed, high-carbohydrate foods and saturated fats frequently.</li>
                                <li>Certain medications, like long-term steroid use.</li>
                                <li>Hormonal disorders, like hypothyroidism and Cushing syndrome.</li>
                                <li>Chronic stress and a lack of quality sleep.</li>
                            </ul>
                        </div>
                    </div>
                </div>
            </div>
