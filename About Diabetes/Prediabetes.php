                <div class="col-md-8">
                    <h3>How can I reverse prediabetes?</h3>
                    <p>The best way to treat — and potentially reverse — prediabetes is
                        through <span> healthy lifestyle changes</span>. Regularly eating nutritious foods
                        and getting regular exercise can help return your blood sugar to
                        healthy levels and prevent or delay Type 2 diabetes.
                        Even <span>small changes</span> can significantly <span>lower your risk</span> for developing
                        Type 2 diabetes, like:</p>
                    <ul class="pl-3">
                        <li> <b>Weight loss </b> : Your healthcare provider may recommend trying to lose excess weight to combat insulin resistance and prediabetes. One study revealed that losing 7% of weight can reduce the onset of Type 2 diabetes by 58%.</li>
                        <li> <b> Regular activity:</b> : Getting regular amounts of moderate-intensity physical activity helps increase glucose usage and improve muscle insulin sensitivity. A single session of moderate-intensity exercise can increase glucose uptake from your blood and into your muscles by at least 40%. This helps lower blood sugar levels. Aim for 30 minutes a day, five days a week, for a total of 150 minutes a week. Try walking or another activity you enjoy.</li>
                        <li> <b> Eating changes:</b> Cutting out added sugars, swapping simple carbohydrates for complex carbohydrates and eating more veggies can help your blood sugar return to healthy levels. Your provider will help you find which long-term diet for prediabetes is best for you.</li>
                    </ul>
                    <h6>Lowering your risk factors for prediabetes can often get your blood
                        sugar levels back to healthy levels. You might:</h6>
                    <ul class="pl-3 pt-2">
                        <li>
                            Work with a <span>nutritionist</span> or dietitian to plan <span>healthy eating</span> patterns you can stick with long-term, like the Mediterranean diet.
                        </li>
                        <li>Find ways to reduce or <span>manage stress</span>.</li>
                        <li><span>Quit smoking</span></li>
                        <li>Get a diagnosis for and/or treat any sleep disorders.</li>
                        <li>Manage related conditions, like <span>high cholesterol</span> and <span>high blood pressure</span>.</li>
                        <li>Find support groups where you can meet other people going through the same challenges.</li>
                    </ul>
                </div>
