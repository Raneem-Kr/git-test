                    <h2>Risk Factors</h2>
                    <h3>What are the risk factors for prediabetes?</h3>
                    <p>Risk factors for prediabetes include:</p>
                    <ul class="pl-3">
                        <li>Family history of Type 2 diabetes (parent or sibling).</li>
                        <li>Having overweight or obesity (a BMI greater than 25).</li>
                        <li>Being physically active fewer than three times a week.</li>
                        <li>Being 45 or older.</li>
                        <li>Smoking</li>
                        <li>Obstructive sleep apnea.</li>
                        <li>Having had gestational diabetes.</li>
                        <li>Polycystic ovarian syndrome (PCOS).</li>
                    </ul>
                    <p>Race and ethnicity are also factors. You’re at increased risk if you are:</p>
                    <ul class="pl-3">
                        <li>Black.</li>
                        <li>Hispanic/Latino American.</li>
                        <li>Native American.</li>
                        <li>Pacific Islander.</li>
                        <li>Asian American.</li>
                    </ul>
                    <p>Some of these risk factors you can’t change, like your age and
                        family history (genetics). But others, like <span>physical inactivity</span> and
                        <span>smoking</span>, you can help improve. The more of these risk factors you
                        have, the more likely prediabetes is around the corner — or you
                        already have it.
                        It’s important to talk to your primary <span>care provider</span> about screening
                        for prediabetes. While it might be mentally easier to avoid finding
                        out, knowing and <span>taking action</span> are very valuable to your long-term
                        health.
                    </p>
