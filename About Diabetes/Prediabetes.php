                        <span>Prediabetes</span> is <span>a serious health condition where </span><span>blood sugar levels</span>
                        are <span>higher than normal</span>, <span>but not</span> high <span>enough</span> yet to be diagnosed as
                        type 2 diabetes.Prediabetes puts you at <span>increased risk</span> of developing
                        <span>type 2 diabetes</span>, <span>heart disease, and stroke.</span>
                    </p>
