                    <h3>Can you take medication for prediabetes?</h3>
                    <p>In some cases, your healthcare provider may recommend taking certain
                        <span>oral diabetes medications</span>. This is more likely if lifestyle changes
                        haven’t helped improve your blood sugar levels and/or you have
                        multiple risk factors for Type 2 diabetes.
                        The most common medications providers prescribe for prediabetes are
                        <span>metformin</span> and <span>acarbose</span>.
                    </p>
