                <h2>Potential Health Risks</h2>
                <h3>What health problems can people with type 1 diabetes develop?</h3>
                <p>Over time, high blood glucose leads to problems such as:</p>
                <ul class="pl-3">
                    <li>heart disease</li>
                    <li>stroke</li>
                    <li>kidney disease</li>
                    <li>eye problems</li>
                    <li>dental disease</li>
                    <li>nerve damage</li>
                    <li>foot problems</li>
                    <li>depression</li>
                    <li>sleep apnea</li>
                </ul>
                <h6>If you have type 1 diabetes, you can help prevent or delay the health problems of diabetes by managing your blood glucose, blood pressure, and cholesterol, and following your self-care plan.</h6>
