                <h1>Type 1 Diabetes</h1>
                <hr>
                <h3>What is type 1 diabetes?</h3>
                <p><span>Diabetes occurs when </span> your <span>blood glucose</span>, also called blood sugar, is <span>too high</span>. Blood glucose is your main source of energy and comes mainly from the food you eat. <span>Insulin</span>, a hormone made by the pancreas, <span>helps</span> the <span>glucose</span> in your blood get <span>into your cells </span>to be used <span>for energy</span>. Another hormone, <span>glucagon</span>, works with insulin to <span>control blood glucose</span> levels. In most people with type 1 diabetes, the body’s <span>immune system</span>, which normally fights infection, <span>attacks</span> and <span>destroys the cells in the pancreas</span> that make insulin. As a <span>result</span>, your <span>pancreas stops making insulin</span>. <span>Without insulin</span>, <span>glucose can’t</span> get into your cells and your <span>blood glucose rises</span> above normal. People with type 1 diabetes need to <span> take insulin </span>every day to stay alive.</p>
