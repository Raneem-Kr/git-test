                <h3>Who is more likely to develop type 1 diabetes?</h3>
                <p>Type 1 diabetes typically occurs in <span>children</span> and <span>young adults</span>, although it can appear at any age. Having a <span>parent </span>or <span>sibling</span> with the disease may increase your chance of developing type 1 diabetes.
