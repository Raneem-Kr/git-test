                <h2>Symptoms & Causes</h2>
                <h3>What are the symptoms of type 1 diabetes?</h3>
                <p>Symptoms of type 1 diabetes are serious and usually <span>happen quickly</span>, over a few days to weeks. Symptoms can include:</p>
                <ul class="pl-3">
                    <li>
                        increased thirst and urination
                    </li>
                    <li>increased hunger</li>
                    <li>blurred vision</li>
                    <li>fatigue</li>
                    <li>unexplained weight loss</li>
                </ul>
                <p>Sometimes the first symptoms of type 1 diabetes are signs of a life-threatening condition called diabetic ketoacidosis (DKA). Some symptoms of DKA include:</p>
                <ul class="pl-3">
                    <li>
                        breath that smells fruity
                    </li>
                    <li>dry or flushed skin</li>
                    <li>nausea or vomiting</li>
                    <li>stomach pain</li>
                    <li> trouble breathing </li>
                    <li>trouble paying attention or feeling confused</li>
                </ul>
                <p>DKA is serious and dangerous. If you or your child have symptoms of DKA, contact your health care professional right away, or go to the nearest hospital emergency room.</p>
