                <h3>What causes type 1 diabetes?</h3>
                <p>Experts think type 1 diabetes is caused by <span>genes</span> and <span>factors in the environment</span>, such as <span>viruses</span>, that might trigger the disease. Researchers are working to pinpoint the causes of type 1 diabetes through studies such as TrialNet.</p>
