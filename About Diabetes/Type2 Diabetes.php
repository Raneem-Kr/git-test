                    <h1 class="pt-4 pb-2">Type 2 Diabetes</h1>
                    <hr>
                    <h3 class="pt-3">What is type 2 diabetes?</h3>
                    <p>
                        Type 2 diabetes is a condition that happens because of a <span>problem</span> in <span>the
                            way the body regulates</span> and <span>uses sugar as a fuel</span>. That sugar also is called
                        <span>glucose</span>. This long-term condition results in <span>too much sugar</span> circulating in
                        the blood. Eventually, high blood sugar levels can lead to <span>disorders of
                            the circulatory</span>, <span>nervous</span>, and <span>immune systems</span>. In type 2 diabetes, there
                        are primarily two problems. The pancreas does not produce <span>enough insulin</span> —
                        a hormone that <span>regulates</span> the movement of <span>sugar</span> into the cells. And cells
                        <span> respond poorly to insulin</span> and take in less sugar. Type 2 diabetes used to
                        be known as <span>adult-onset</span> diabetes, but both type 1 and type 2 diabetes can
                        begin during childhood and adulthood. Type 2 is more common in <span>older
                            adults</span>. But the increase in the number of children with obesity has led to
                        more cases of type 2 diabetes in younger people. There's <span>no cure</span> for type
                        2 diabetes. <span>Losing weight</span>, <span>eating well</span> and <span>exercising </span>can help manage the
                        disease. If diet and exercise aren't enough to control blood sugar,
                        diabetes medications or insulin therapy may be recommended.
                    </p>
                    <h3>Who is more likely to develop type 2 diabetes?</h3>
                    <p>
                        You can develop type 2 diabetes at <span>any age</span>, even during childhood.
                        However, type 2 diabetes occurs most often in middle-aged and older
                        people. You are more likely to develop type 2 diabetes if you are agesp
                        45
                        or older, have a <span>family history</span> of diabetes, or are <span>overweight</span> or have
                        <span>obesity</span>. Diabetes is more common in people who are <span>African American</span>,
                        <span>Hispanic/Latino</span>,<span> American Indian</span>, <span>Asian American</span>, or <span>Pacific Islander</span>.
                        Physical inactivity and certain health problems such as <span>high blood
                            pressure</span> affect your chances of developing type 2 diabetes. You are also
                        more likely to develop type 2 diabetes if you have prediabetes or had
                        gestational diabetes when you were pregnant. Learn more about risk factors
                        for type 2 diabetes.
                    </p>
                    <h2>Symptoms & Causes</h2>
                    <h3>What are the symptoms of type 2 diabetes?</h3>
                    <p>
                        Symptoms of type 2 diabetes often develop <span>slowly</span>. In fact, you can be
                        living with type 2 diabetes for years and not know it. When symptoms are
                        present, they may include:
                    </p>
                    <ul class="pl-3">
                        <li>Increased thirst.</li>
                        <li>Frequent urination</li>
                        <li>Increased hunger.</li>
                        <li>Unintended weight loss.</li>
                        <li>Fatigue.</li>
                        <li>Blurred vision.</li>
                        <li>Slow-healing sores</li>
                        <li>Frequent infections.</li>
                        <li>Numbness or tingling in the hands or feet.</li>
                        <li>Areas of darkened skin, usually in the armpits and neck.</li>
                    </ul>
                    <h3>What causes type 2 diabetes?</h3>
                    <p>Type 2 diabetes is mainly the result of <span>two problems</span>:</p>
                    <ul class="pl-3">
                        <li>
                            Cells in muscle, fat, and the liver become resistant to insulin As a
                            result, the cells don't take in enough sugar.
                        </li>
                        <li>
                            The pancreas can't make enough insulin to keep blood sugar levels within
                            a healthy range.
                        </li>
                    </ul>
                    <p>
                        Exactly why this happens is not known. Being overweight and inactive are
                        key contributing factors.
                    </p>
                    <h2>Potential Health Risks</h2>
                    <h3>What health problems can people with diabetes develop?</h3>
                    <p>
                        Following a good diabetes care plan can help protect against many
                        diabetes-related health problems. However, if not managed, diabetes can
                        lead to problems such as <span> heart disease and stroke</span>, <span>nerve damage, </span> <span>kidney
                            disease, </span> <span>foot problems, </span> <span>eye disease, </span> <span>gum disease, </span> and other dental problems.
                        <span>Sexual</span> and <span>bladder</span> problems. Many people with type 2 diabetes also have
                        <span>nonalcoholic fatty liver disease </span>(NAFLD). Losing weight if you are
                        <span>overweight</span> or havesp
                        obesity can improve <span>NAFLD</span>. Diabetes is also linked to
                        other health problems such as <span>sleep apnea</span>, <span>depression</span>, some types of
                        <span>cancer</span>, and <span>dementia</span>.
                    </p>
                    <h2>Medications for Managing Type 2</h2>
                    <h3>What medicines do I need to treat my type 2 diabetes?</h3>
                    <p>
                        Along with following your diabetes care plan, you may need diabetes
                        medicines, which may include <span>pills</span> or <span>medicines</span> you inject under your
                        skin, such as <span>insulin</span>. Over time, you may need more than one diabetes
                        medicine to manage your blood glucose. Even if you don’t take insulin, you
                        may need it at special times, such as during pregnancy or if you are in
                        the hospital. You also may need <span> medicines for high blood pressure</span>, <span>high
                            cholesterol</span>, or other conditions.
                    </p>
                    <h2>Byond Medication: Comprehensive Management </h2>
                    <h3>How can I lower my chances of developing type 2 diabetes?</h3>
                    <p>
                        Research such as the Diabetes Prevention Program External link, sponsored
                        by the National Institutes of Health, has shown that you can take steps to
                        reduce your chances of developing type 2 diabetes if you have risk factors
                        for the disease. Here are some things you can do to lower your risk:
                    </p>
                    <ul class="pl-3">
                        <li>
                            <span>Lose weight</span>: if you are overweight, and keep it off. You
                            may be able to prevent or delay diabetes by losing 5 to 7 percent of
                            your current weight.1 For instance, if you weigh 200 pounds, your goal
                            would be to lose about 10 to 14 pounds.
                        </li>
                        <li>
                            <span>Move more</span>: Get at least 30 minutes of physical activity,
                            such as walking, at least 5 days a week. If you have not been active,
                            talk with your health care professional about which activities are best.
                            Start slowly and build up to your goal.
                        </li>
                        <li>
                            <span>Eat healthy foods</span>: Eat smaller portions to reduce the
                            amount of calories you eat each day and help you lose weight. Choosing
                            foods with less fat is another way to reduce calories. Drink water
                            instead of sweetened beverages.
                        </li>
                    </ul>
                    <h6>
                        Ask your health care team what other changes you can make to prevent or
                        delay type 2 diabetes.
                        Most often, your best chance for preventing type 2 diabetes is to make
                        lifestyle changes that work for you long term.
                    </h6>

