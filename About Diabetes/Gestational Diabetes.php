            <div class="row">
                <div class="col-md-8">
                    <h3 style="margin-top:-20px;">Symptoms & Causes</h3>


                    <p>
                        Gestational diabetes often has <span>no symptoms</span>, or they may be mild, such as
                        being thirstier than normal or having to urinate more often. Gestational
                        diabetes is sometimes <span> related </span>to the <span> hormonal changes </span>of pregnancy that
                        make your body less able to use insulin. <span>Genes</span> and <span>extra weight</span> may also
                        play a role.
                    </p>
                </div>
            </div>
            <div class="col-md-8">
                <div class="row">
                    <h3>Tests & Diagnosis</h3>

                    <p>
                        Your doctor will test you for gestational diabetes between 24 and 28 weeks
                        of pregnancy. Tests include the <span>glucose challenge</span> test and the <span>oral
                            glucose tolerance</span> test (OGTT). If the results of the glucose challenge
                        test show high blood glucose, you will return for an OGTT test to confirm
                        the diagnosis of gestational diabetes.
                    </p>
                </div>
            </div>


            <div class="col-md-8">
                <div class="row">
                    <h3>Management & Treatment</h3>

                    <p>
                        Managing gestational diabetes includes following a <span>healthy eating</span> plan and
                        being <span>physically active</span>. If your eating plan and physical activity aren’t
                        enough to keep your blood glucose in your target range, you may need
                        <span> insulin</span>.
                    </p>
                </div>
            </div>

            <div class="col-md-8">
                <div class="row">
                    <h3>Prevension</h3>

                    <p>
                        You can <span> lower</span> your chance of getting gestational diabetes by <span>losing extra
                            weight</span> before you get pregnant if you are overweight. Being <span>physically active</span>
                        before and during pregnancy also may help <span>prevent</span> gestational
                        diabetes.
                    </p>
                </div>
            </div>


            <div class="col-md-8">
                <div class="row">
                    <h3>After Your Baby is Born</h3>

                    <p>
                        If you had gestational diabetes, you are more likely to develop type 2
                        diabetes. Your child is more likely to have obesity or develop type 2
                        diabetes. You may be able to lower your and your child’s chances of
                        developing these problems by reaching a <span>healthy weight</span>, <span>making healthy
                            food choices</span>, and <span>being physically active</span>.
                    </p>
                </div>
            </div>
        </div>


