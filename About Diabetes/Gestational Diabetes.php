                    <h1>Gestaional Diabetes</h1>
                    <hr>
                    <h3>What is Gestational Diabetes?</h3>
                    <p>
                        Gestational diabetes is a type of diabetes that occurs only during
                        <span> pregnancy</span>. Gestational diabetes can cause <span>health problems</span> in both mother
                        and baby. Managing your diabetes can help protect you and your baby.
                    </p>
                </div>
                <div class="col-md-5">
                    <img class="img4 ml-5 pl-5 pt-5 mt-5" src="../CSS/images/the-meter-3310287_1280.jpg" alt="" />
