                                <h2>
                                    How Is Diabetes Treated?
                                </h2>
                                <h6>There's <span>no cure</span> for diabetes, but it can be <span>managed</span> and <span>controlled</span>. The goals of managing diabetes are to:</h6>
                                <ul class="pl-3 whatPageList">
                                    <li class="list"> <span>Keep your blood sugar levels as near to normal as possible</span> by balancing food intake with medication and activity.</li>
                                    <li><span>Maintain your blood cholesterol and triglyceride (lipid) levels as near their normal ranges as possible</span> by avoiding added sugars and processed starches and by reducing saturated fat and cholesterol.</li>
                                    <li><span>Control your blood pressure.</span> Your blood pressure should not go over 130/80.</li>
                                    <li><span>Slow or possibly prevent the development of diabetes-related health problems</span>.</li>
                                </ul>

                                <h6>You hold the key to managing your diabetes by:</h6>
                                <ul class="pl-3 whatPageList">
                                    <li><span>Planning what you eat</span> and <span>following a balanced meal plan</span>.</li>
                                    <li><span>Exercising regularly</span></li>
                                    <li><span>Taking medicine, if prescribed,</span> and closely following the guidelines on how and when to take it.</li>
                                    <li><span>Monitoring your blood sugar and blood pressure levels at home.</span></li>
                                    <li>Keeping your appointments with your health care providers and having laboratory tests as ordered by your doctor.</li>
                                </ul>
                                <h6>Remember: What you do <span>at home </span>every day affects your <span>blood sugar</span> more than what your doctor can do every few months during your checkups.</h6>
