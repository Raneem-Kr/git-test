                        <h2>Types of Diabetes</h2> <br>
                        <h6>The most common types of diabetes are type 1, type 2, and gestational diabetes.</h6>
                        <ul class="p-3 list">
                            <li>
                                <a href="../About Diabetes/Type1 Diabetes.php">Type 1 diabetes</a> <br>
                                <p> If you have type 1 diabetes, your body makes <span>little</span> or <span>no insulin</span>. Your immune system attacks and destroys the cells in your pancreas that make insulin. Type 1 diabetes is usually diagnosed in <span>children</span> and <span>young adults</span>, although it <span>can</span> appear at <span>any age</span>. People with type 1 diabetes need to take <span>insulin every day</span> to stay alive.</p>

                            </li>
                            <li>
                                <a href="../About Diabetes/Type2 Diabetes.php">Type 2 diabetes</a>
                                <p>If you have type 2 diabetes, the cells in your body <span>don’t</span> use insulin properly. The pancreas may be making insulin but is not making <span>enough insulin</span> to keep your blood <span>glucose level</span> in the <span> normal</span> range. Type 2 diabetes is the <span>most common type</span> of diabetes. You are more likely to develop type 2 diabetes if you have risk factors, such as <span>overweight</span> or <span>obesity</span>, and a family history of the disease. You can develop type 2 diabetes at any age, even during childhood. You can help <span>delay</span> or <span>prevent</span> type 2 diabetes by knowing the <span>risk factors</span> and taking steps toward a <span>healthier lifestyle</span>, such as losing weight or preventing weight gain.</p>
                            </li>
                            <li>
                                <a href="../About Diabetes/Gestational Diabetes.php">Gestationla diabetes</a>
                                <p>Gestational diabetes is a type of diabetes that develops during <span>pregnancy</span>. Most of the time, this type of diabetes <span>goes away</span> <span>after</span> the baby is <span>born</span>. However, if you’ve had gestational diabetes, you have a higher chance of developing type 2 diabetes later in life. Sometimes diabetes diagnosed during pregnancy is type 2 diabetes.</p>
                            </li>
                            <li>
                                <a href="../About Diabetes/Prediabetes.php">Prediabetes</a>
                                <p>People with prediabetes have blood glucose levels that are <span>higher</span> than <span>normal</span> but not high enough to be diagnosed with type 2 diabetes. If you have <span>prediabetes</span>, you have a sprisk of developing type 2 diabetes in the future. You also have a higher risk for heart disease than people with normal glucose levels.</p>
                                <p>People with prediabetes have blood glucose levels that are <span>higher</span> than <span>normal</span> but not high enough to be diagnosed with type 2 diabetes. If you have <span>prediabetes</span>, you have a <span></span>risk of developing type 2 diabetes in the future. You also have a higher risk for heart disease than people with normal glucose levels.</p>
                            </li>
                            <li>Other types of Diabetes</li>
                            <p>A less common type of diabetes, called <span>monogenic diabetes</span>, is caused by a <span>change</span> in a <span>single gene</span>. Diabetes can also come from having surgery to <span>remove the pancreas</span>, or from <span>damage</span> to the pancreas due to conditions such as cystic fibrosis NIH external link or pancreatitis.</p>
