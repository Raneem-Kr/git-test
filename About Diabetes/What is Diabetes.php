                        <h2 class="mb-3">What is Diabetes?</h2>
                        <p> <span>Diabetes</span> is a disease that <span>occurs when</span> your <span>blood glucose</span>, also called blood sugar, is <span>too high</span>. <span>Glucose is</span> your body’s <span>main source of energy</span>. Your body can make glucose, but glucose also comes from the food you eat. <span>Insulin is</span> a <span>hormone</span> made by the pancreas <span>that helps glucose get into your cells to be used for </span><span>energy</span>. If you have diabetes, your body doesn’t make <span>enough—or any—insulin</span>, or <span>doesn’t use insulin properly</span>. Glucose then stays in your blood and doesn’t reach your cells. Diabetes raises the risk for damage to the eyes, kidneys, nerves, and heart. Diabetes is also linked to some types of cancer. Taking steps to prevent or manage diabetes may lower your risk of developing diabetes health problems.</p>
