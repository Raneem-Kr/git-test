                <h2>Diabetes & CKD Foods</h2>
                <p> Below are just a few examples of <span>foods a person with both diabetes and CKD can eat</span>. Your dietitian can give you lots more suggestions and help you find recipes for tasty meals: <br>
                <ul class="pl-3">
                    <li><span>Fruits:</span> berries, grapes, cherries, apples, plums</li>
                    <li><span>Veggies:</span> cauliflower, onions, eggplant, turnips</li>
                    <li><span>Proteins:</span> lean meats (poultry, fish), eggs, unsalted seafood</li>
                    <li><span>Carbs</span>: white bread, bagels, sandwich buns, unsalted crackers, pasta</li>
                    <li><span>Drinks</span>: water, clear diet sodas, unsweetened tea</li>
                </ul>
                Here’s one way your CKD diet and diabetes diet can work together: If you drink orange juice to treat low blood sugar, switch to kidney-friendly apple or grape juice. You’ll get the same blood-sugar boost with a lot less potassium.
                </p>
                <h2>
                    Late-Stage CKD
                </h2>

                <p>
                    Your <span>nutrition needs</span> will <span>change with late-stage CKD</span>. If you’re on <span>dialysis</span>, you may need to <span>eat more,</span> especially more protein. Your appetite can change because food tastes different. <br>
                    Dialysis filters your blood like kidneys do, but it doesn’t work as well as healthy kidneys. <span>Fluid can build up</span> in your <span>body between treatments</span>. You may need to limit how much fluid you drink, and watch for swelling around your eyes or in your legs, arms, or belly. <br>
                    Your <span>blood sugar levels can</span> actually <span>get better with late-stage CKD</span>, possibly because of changes in how your body uses insulin. But when you’re on dialysis, your blood sugar can increase because the fluid used to filter your blood is high in glucose (sugar). Your need for insulin and other diabetes medicines will be hard to predict, so your doctor will monitor you closely.
                </p>
                <h2>See Your Dietitian</h2>
                <p> <span>CKD and diabetes both change over time</span>, and so will your diet. Be sure to <span>check in with your dietitian </span>as recommended. You’ll get the support and confidence you need to manage your meals, solve any problems, and be your healthiest.</p>
