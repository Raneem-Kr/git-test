                <h1>Foods to Limit</h1>
                <p><span>Eat less salt/sodium.</span> That’s a good move for diabetes and really important for CKD. Over time, your kidneys lose the ability to control your sodium-water balance. <span>Less sodium </span> in your diet will <span>help lower blood pressure and decrease fluid buildup </span>in your body, which is common in kidney disease. <br>
                    <span>Focus on fresh, homemade food</span> and <span>eat only small amounts of restaurant food </span>and packaged food, which usually have lots of sodium. <span>Look for low sodium (5% or less) on food labels</span>. <br>
                    <span>In a week or two</span>, you’ll <span>get used to less salt</span> in your food, especially if you dial up the flavor with herbs, spices, mustard, and flavored vinegars. But don’t use salt substitutes unless your doctor or dietitian says you can. Many are very high in potassium, which you may need to limit. <br>
                    <span>Depending on your stage of kidney</span> disease, you may also need to <span>reduce the potassium, phosphorus, and protein</span> in your diet. Many foods that are part of a typical healthy diet may not be right for a CKD diet. <br>
                    <span> Phosphorus </span>is a mineral that <span>keeps your bones strong</span> and other parts of your body healthy. Your <span>kidneys can’t remove extra phosphorus</span> from your blood very well. Too much <span>weakens bones </span>and can <span>damage your blood vessels, eyes, and heart</span>. <span>Meat, dairy, beans, nuts, whole-grain bread, and dark-colored sodas are high in phosphorus.</span> Phosphorus is also added to lots of packaged foods <br>
                    The <span>right level of potassium keeps your nerves and muscles working well</span>. With CKD, <span>too much potassium </span>can build up in your blood and <span>cause serious heart problems</span>. <span>Oranges, potatoes, tomatoes, whole-grain bread, and many other foods</span> are <span>high in potassium.</span> Apples, carrots, and white bread are lower in potassium. Your doctor may prescribe a potassium binder, a medicine that helps your body get rid of extra potassium. <br>
                    <span> Eat the right amount of protein.</span> <span>More protein</span> than you need <span>makes your kidneys work harder and may make CKD worse.</span> But <span>too little isn’t healthy either.</span> Both animal and plant foods have protein. Your dietitian can help you figure out the right combination and amount of protein to eat. <br>
                </p>
