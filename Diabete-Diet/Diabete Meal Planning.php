                <h2>About Portion Size</h2>
                <p>
                    <span>Portion size</span> and <span>serving size</span> aren’t always the same. A <span>portion</span> is <span>the amount</span> of food you choose to eat at one time, while a <span>serving</span> is a <span>specific amount</span> of food, such as one slice of bread or 8 ounces (1 cup) of milk. <br>
                    These days, portions at restaurants are quite a bit larger than they were several years ago. One entrée can equal 3 or 4 servings! Studies show that people tend to eat more when they’re served more food, so getting portions under control is really important for managing weight and blood sugar. <br>
