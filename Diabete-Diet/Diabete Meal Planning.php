                <h2>The Plate Method</h2>
                <p>
                    It’s easy to eat <span>more food</span> than you need <span>without realizing</span> it. The <span>plate method</span> is a simple, visual way to <span>make sure</span> you get <span>enough nonstarchy</span> <span>vegetables</span> and <span>lean protein</span> while <span>limiting </span>the amount of <span>higher-carb</span> foods you eat that have the highest impact on your blood sugar. <br>
                    Start with <span>a 9-inch dinner plate</span> (about the length of a business envelope):
                <ul class="pl-3">
                    <li>Fill <span>half</span> with <span>nonstarchy vegetables</span>, such as salad, green beans, broccoli, cauliflower, cabbage, and carrots.</li>
                    <li>Fill <span>one quarter</span> with a <span>lean protein</span>, such as chicken, turkey, beans, tofu, or eggs.</li>
                    <li>Fill <span>one quarter</span> with <span>carb foods</span>. Foods that are higher in carbs include grains, starchy vegetables (such as potatoes and peas), rice, pasta, beans, fruit, and yogurt. A cup of milk also counts as a carb food.</li>
                </ul>
                Then choose <span>water</span> or a <span>low-calorie</span> drink such as unsweetened iced tea to go with your meal.
                </p>
