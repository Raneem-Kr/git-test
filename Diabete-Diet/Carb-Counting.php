                    1 cup low-fat milk (13g) <br>
                    2/3 medium banana (20g) <br>
                    ¼ cup chopped walnuts (4g) <br>
                    Total carbs: 65 grams, about 4 carb servings <br></p>
                <h6>Lunch</h6>
