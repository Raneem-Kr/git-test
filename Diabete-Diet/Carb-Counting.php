                <h2>How are carbs measured?</h2>
                <p>carbs are <span>measured </span>in <span>grams.</span> On packaged foods, you can find total carb grams on the Nutrition Facts label. You can also check this list or use a carb-counting app to find grams of carbs in foods and drinks.</p>
                <p>For diabetes meal planning,<span> 1 carb serving is about 15 grams</span> of carbs. This isn’t always the same as what you think of as a serving of food. For example, most people would count a small baked potato as 1 serving. However, at about 30 grams of carbs, it counts as 2 carb servings.</p>
                <p>Try to <span>eat</span> about the <span>same amount of carbs</span> at <span>each meal</span> to <span>keep</span> your <span>blood sugar levels steady</span> throughout the day (not necessary if you use an insulin pump or give yourself multiple daily injections—you’ll take a fast-acting or short-acting insulin at mealtimes to match the amount of carbs you eat).</p>
                <p> This sample menu has about <span> 1,800 calories</span>, <span>200 grams of carbs</span>, and about <span> 13 carb servings</span>.</p>
                <h6>Breakfast</h6>
