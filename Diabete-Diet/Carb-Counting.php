                <h1>Carb Counting</h1>
                <p><span>Counting carbohydrates</span>, or carbs—keeping track of the carbs <span>in all your meals, snacks, and drinks</span>—can <span>help you </span><span>match</span> your <span>activity level</span> and <span>medicines</span> to the food you eat. Many people with diabetes count carbs to make managing blood sugar easier, which can also help them:
                    <br>
                <ul class="pl-3">
                    <li>Stay <span>healthy</span> longer.</li>
                    <li><span>Feel better</span> and improve their quality of life.</li>
                    <li><span>Prevent or delay diabetes complications </span>such as kidney disease, eye disease, heart disease, and stroke.</li>
                </ul>
                If <span>you take mealtime insulin</span>, you’ll <span>count carbs</span> to <span>match your insulin dose</span> to the amount of carbs in your foods and drinks. You may also take additional insulin if your blood sugar is higher than your target when eating.
                </p>
                <h2>What are the different types of carbs?</h2>
                <p>There are 3 types of carbs:</p>
                <ol class="pl-3 number ">
                    <li><span> Sugars,</span> such as the natural sugar in fruit and milk or the added sugar in soda and many other packaged foods.</li>
                    <li><span>Starches,</span> including wheat, oats, and other grains; starchy vegetables such as corn and potatoes; and dried beans, lentils, and peas.</li>
                    <li><span>Fiber,</span> the part of plant foods that isn’t digested but helps you stay healthy.</li>
                </ol>
