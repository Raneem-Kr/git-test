                <h1 class="py-3">Eating Out
                </h1>
                <p>
                    Three in five Americans say they eat dinner out at least once a week, and as you’ve probably noticed, <span>restaurant portions</span> have gotten <span>a lot bigger</span>. Unless you have a plan in place, that can be a recipe for regular overeating. <br>
                    While you can’t directly control the way the food is prepared or the calories in each dish, you can <span> plan ahead</span>, <span> ask questions</span>, and <span>order food</span> that both <span>tastes good</span> and is <span>good</span> for you. With practice, it’ll get easier to separate the healthier choices from the not so healthy and to keep portions under control. Some tips:
                </p>
                <h2>Plan Ahead</h2>

                <ul class="pl-3">
                    <li><span>Decide what</span> you’re going <span> to order</span> before you go so you don’t feel rushed or tempted by less healthy choices.</li>

                    <li>If your <span>meal</span> will be <span>later than normal</span>, have a snack that <span>contains fiber and protein</span> a small handful of nuts is a great choice—before you go out to avoid getting too hungry. Then cut back by that amount when you eat your meal</li>
                </ul>
                <h2>Start Smart</h2>
                <ul class="pl-3">
                    <li><span>Drink a big glass of water</span> as soon as you sit down. It’s been shown to help you eat less.</li>
                    <li>Skip the chips and salsa or bread that comes to your table before you order. Better yet, ask your server to remove them.</li>
                </ul>

                <h2>Order Wisely</h2>
                <ul class="pl-3">
                    <li><span>Choose baked, steamed, grilled, or broiled</span> instead of fried, breaded, crispy, or creamy to reduce fat and calorie</li>
                    <li>Ask for <span>veggies instead of fries </span>or other high-calorie side dishes.</li>
                    <li>Avoid items that seem healthy but aren’t, such as salads loaded with dressing, cheese, croutons, and bacon.</li>

                    <li><span>Avoid added sugar</span>—don’t order dishes that have BBQ, glazed, sticky, honey, or teriyaki in their name.</li>
                    <li>Order sauces, salad dressing, and spreads on the side and use sparingly. Try dipping your fork in the dressing before taking each bite to enjoy the flavor for fewer calories.</li>
                </ul>

                <h2>Share and Savor</h2>
                <ul class="pl-3">
                    <li>Share your main dish or <span> eat half and wrap up the rest</span> for later.</li>
                    <li><span>Enjoy the occasional sweet</span> treat (minus the guilt) <span>by cutting back on carbs during your meal</span>, and get a dessert to share with the table. You won’t miss out—the first few bites are usually the tastiest.
                    </li>
                </ul>

